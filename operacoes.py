def somar(num1,num2):
    return num1+num2  #pode retornar o resultado direto!

def subtrair(num1,num2):
    result=num1-num2
    return result

def multiplicar(num1,num2):
    result=num1*num2
    return result

def dividir(num1,num2):
    if num2==0:
        return None
    else:
        result=num1/num2
    return result